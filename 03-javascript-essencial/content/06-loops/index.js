/* Loop com while */
console.log ("while: 1 até 10...");
var i = 1;

while (i <= 10)
{
    console.log ("valor de i = " + i);
    i++;
}


/* Loop com do-while */
console.log ("do while: 1 até 10...");
var j = 1;

do
{
    console.log ("valor de j = " + j);
    j++;
}
while (j <= 10);


/* Loop com for */
console.log ("for: 1 até 10...");

for (var k = 1; k <= 10; k++) 
{
    console.log ("valor de k = " + k);
}